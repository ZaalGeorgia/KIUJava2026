package lesson260309;

public class Kingdom {

	private String king;
	private String castle;
	private String army;

	String getArmy() {
		return army;
	}

	void setArmy(String army) {
		this.army = army;
	}

	String getCastle() {
		return castle;
	}

	void setCastle(String castle) {
		this.castle = castle;
	}

	String getKing() {
		return king;
	}

	void setKing(String king) {
		this.king = king;
	}

}
